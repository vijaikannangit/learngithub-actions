"""
Package Creation in Python.
This Module is developed as a Demo Package for testing python package.
This Module calculates sum, average and power for the arguments.
"""

def sum(x,y):
    """
    Sum of given arguments.
    Keyword arguments:
    -----------------
    x : number format.
    y : number format.
    Returns:
    --------
    x+y : Number.
        Returns sum of two Numbers.
    """   
    return x+y

def average(x,y):
    """
    Average of given arguments.
    Keyword arguments:
    -----------------
    x : number format.
    y : number format.
    Returns:
    --------
    x+y : Number.
        Returns Average of two Numbers.
    """   
    return (x+y)/2

def power(x,y):
    """
    Power according to given arguments.
    Keyword arguments:
    -----------------
    x : number format.
    y : number format.
    Returns:
    --------
    x+y : Number.
        Returns x power y.
    """   
    return x**y